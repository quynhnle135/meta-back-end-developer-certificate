my_string = "Hello World"

# get the first character
print(my_string[0])

# get the last character
print(my_string[-1])

# get the second to last character
print(my_string[-2])

# string comprehension
print(my_string[::2])

# concatenation
my_name = "Quinn"
print(f"Hi, my name is {my_name}")

print("Hi, my name is " + my_name)

print("Hi, my name is", my_name)

print(f"The length of my name: {len(my_name)}")
